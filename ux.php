                <p>
                    Als groep hebben wij ons onderverdeeld in 3 deelvragen, samen met Bart hebben wij de deelvraag “Welke bestaande hulpmiddelen bestaan er al voor parkinson patiënten?” op ons genomen.
                    <br><br>
                    Om deze vraag te beantwoorden hebben wij de volgende onderzoeken gedaan:
                    <br><br>
                    <b>Benchmark creation | Bieb</b><br>
                    Door zelf onderzoek doen op het internet en in brochures van het ziekenhuis ben ik (samen met Bart) achter veel hulpmiddelen gekomen waar wij anders nooit op waren gekomen.
                    Wij hebben voor deze methode gekozen omdat wij hiermee snel tot veel resultaten konden komen.
                    <br><br>

                    <b>Expoert interview | Veld</b><br>
                    Om onze inzichten te vergroten en achter informatie te komen die wij zelf niet konden vinden op internet hebben wij er voor gekozen om een interview/ gesprek te houden met een parkinsonverpleegkundige.
                    <br><br>

                    <b>USP</b><br>
                    Door te kijken naar USP’s van andere producten maar ook naar die van ons kunnen wij potentieel de beste dingen uit andere producten halen om ons eigen concept te versterken of verbeteren.
                    <br><br>

                    <b>Day in the life| Veld</b><br>
                    Het mini onderzoek “A day in the life” heeft mij een beter inzicht gegeven in hoe de tremors het dagelijkse leven kunnen beïnvloeden. Simpele taken zoals iets drinken kan al een grote opgave zijn. Als je bijvoorbeeld een onverwachte tremor krijgt tijdens het drinken kan het glas al van je mond los raken waardoor je drinken overal ligt. Dit soort simpele taken worden zo dus al veel moeilijker. Door dit onderzoek heb ik veel meer empathie gekregen voor parkinson patiënten.
                    <br><br>

                    Na afloop van de tweede sprint ben ik er van overtuigd dat wij de juiste methodes hebben gekozen om deze vraag te beantwoorden. Door de combinatie van bieb en veld onderzoek zijn wij tot inzichten gekomen waar wij anders niet op waren gekomen.
                    <br><br>

                    Tijdens ons gesprek met een parkinsonverpleegkundige hebben we veel inzicht gekregen over wat wel en niet werkt bij parkinson patiënten. Zo hadden wij als groep eerst een idee om de patiënten te assisteren met het initiatief te nemen door hun energie te meten en op basis hiervan suggesties te doen voor activiteiten zoals wandelingen. Echter vertelde de verpleegkundige ons dat dit helemaal niet zou kunnen werken. De patiënten hebben echt iemand nodig die hun mee neemt, een simpele notificatie gaat hierbij niet helpen.
                    <br><br>

                    <b>Bezoek Parkinson Café - 11 april</b><br>
                    Tijdens het bezoek aan het Parkinson Café in eindhoven op 11 april heb ik, net zoals iedereen uit de proftaak groep, <a href="https://xd.adobe.com/view/f1faff5a-64dc-400a-47e7-befa34913d4b-87cb/?fullscreen" target="_blank">Prototype gate 2</a> getest op de doelgroep. Hierbij heb ik kort vooraf wat vragen gesteld over de gebruiker, mijzelf voorgesteld en verteld waar ons product voor dient. Doordat ik van te voren heb nagedacht over hoe ik het ging aanpakken merkte ik wel degelijk verschil met voorgaande tests uit andere semesters. Ik kreeg veel gerichter feedback op het product. Feedback waar wij ook duidelijk iets mee kunnen. In totaal is er bij het cafe getest met ongeveer 6 personen. De verwerkte resultaten zijn <a href="https://docs.google.com/document/d/15PQBcwdE32wnlBglgcy5TTSkAmiFFcCCwc10EoTu99E/edit?usp=sharing" target="_blank">hier</a> te vinden.
                    <br><br>
                    Ik ben blij met de inzichten die wij hebben gekregen. Veel hypotheses zijn bevestigd, zoals het gebruik van de kleuren en icoontjes. Ook hebben we ontdekt dat oefeningen niet zo werken als wij hadden gedacht. Maar de testpersonen vinden het nog steeds een nuttige functie.
                    <br><br>
                    Het testen verliep ook goed en we hadden het goed voorbereid. Door vooraf af te spreken wie aantekeningen maakt, wie vragen stelt en wie afbeeldingen/ video’s maakt verliepen de tests heel soepel.

